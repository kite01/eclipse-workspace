package chapter02;

public class Test {
	
	public static void main(String[] args) {
		
		int num = 10;  // 한라인 주석입니다. 
		int num2 = 20;  // 변수 num 에 20을 할당합니다. 
		
		//한 라인 주석의 단축키는 Ctrl+/		
	
		/*
		 * 여러 라인 주석 처리 입니다. 주석은 프로그램에서 해석되지 않는 부분입니다.
		 여러라인 주석의 단축키는 Ctrl+Shift + /
		 여러라인 주석의 해제 단축키는 Ctrl+Shift + \
		 *
		 */
		 
		System.out.println( num ); // num 출력
		System.out.println( num2 ); //num2 출력
		System.out.println(num + num2);
		
			
	}

}
